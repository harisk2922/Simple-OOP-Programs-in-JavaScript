class MathOperations {
    static addition(a, b) {
        return a+b;
    }
    static subtraction(a, b) {
        return a-b;
    }
    static multiplication(a, b) {
        return a*b;
    }
    static division(a, b) {
        return a/b;
    }
}

console.log('Addition: ' + MathOperations.addition(10, 5));
console.log('Subtraction: ' + MathOperations.subtraction(10, 5));
console.log('Multiplication: ' + MathOperations.multiplication(10, 5));
console.log('Addition: ' + MathOperations.division(10, 5));